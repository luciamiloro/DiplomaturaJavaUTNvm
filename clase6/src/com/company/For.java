package com.company;

public class For {
    public static void main(String[] args) {
        // variables se inicializan dentro de la estructura; condicion; incremento/decremento
        for (int i=1; i<=10; i++){
            System.out.println(i);
        }

        for (int j=10; j > 0; j--){
            System.out.println(j);
        }
    }
}
